char	*ft_strlowcase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] = str[i] + 32;
		i++;
	}
	return (str);
}

char	*ft_strcapitalize(char *str)
{
	int	i;
	int	ip;

	i = 0;
	ip = 1;
	ft_strlowcase(str);
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			if (ip == 1)
				str[i] -= 32;
			ip = 0;
		}
		else if (str[i] >= '0' && str[i] <= '9')
			ip = 0;
		else
			ip = 1;
		i++;
	}
	return (str);
}
